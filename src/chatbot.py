BASE_DIR = Path(__file__).resolve().parent.parent
MODEL_PATH = BASE_DIR / "models" / "intent_classifier.pkl"


def normalize_short_query(query: str) -> str:
    """
    Convert very short fragment-like inputs into a definition-style query.
    Examples:
    - 'llm' -> 'What is llm?'
    - 'rag' -> 'What is rag?'
    - 'tokenization' -> 'What is tokenization?'
    """
    q = query.strip()

    if not q:
        return q

    word_count = len(q.split())

    # if it is a short fragment and not already a question, normalize it
    if word_count <= 2 and "?" not in q:
        return f"What is {q}?"

    return query


def rule_based_override(query: str):
    """
    Lightweight rule layer to better capture academic/course-related queries
    that may not be well represented in the CLINC subset.
    """
    q = query.lower()

    # clear out-of-scope cases
    if any(word in q for word in ["joke", "funny", "song", "music", "weather", "traffic", "uber"]):
        return "oos"

    # identity / instructor
    if any(word in q for word in ["instructor", "teacher", "lecturer", "coordinator", "who teaches"]):
        return "agent_identity"

    # grading / exam / project / policy
    if any(word in q for word in [
        "grade", "graded", "grading",
        "exam", "final", "midterm",
        "assignment", "project",
        "attendance", "policy"
    ]):
        return "schedule_query"

    # course / syllabus / content
    if any(word in q for word in [
        "course", "syllabus", "content", "topics", "covered", "curriculum"
    ]):
        return "schedule_query"

    # concept questions
    if any(word in q for word in [
        "what is", "what are", "explain", "difference between",
        "bert", "transformer", "transformers",
        "embedding", "embeddings",
        "attention",
        "tokenization", "tokenizer", "token", "tokens",
        "rag", "fine tuning", "vector database",
        "llm", "llms", "large language model", "large language models",
        "nlp", "ner"
    ]):
        return "concept_query"

    # "why" style concept questions
    if any(word in q for word in [
        "why is tokenization important",
        "why are tokens important",
        "why is bert used",
        "why is rag used",
        "why are embeddings important"
    ]):
        return "concept_query"

    return None


def looks_academic(query: str) -> bool:
    """
    If classifier predicts OOS but the query still looks academic,
    allow retrieval as a fallback.
    """
    q = query.lower()

    academic_keywords = [
        "course", "syllabus", "instructor", "teacher", "lecturer", "coordinator",
        "grade", "grading", "exam", "final", "midterm", "assignment", "project",
        "attendance", "content", "topic",
        "bert", "transformer", "transformers",
        "embedding", "embeddings",
        "attention",
        "tokenization", "tokenizer", "token", "tokens",
        "rag", "fine tuning", "vector database",
        "llm", "llms", "large language model", "large language models",
        "nlp", "ner"
    ]

    return any(word in q for word in academic_keywords)

def build_kb_terms(retriever) -> list[str]:
    terms = set()

    for _, row in retriever.kb.iterrows():
        qv = str(row.get("question_variation", "")).strip().lower()
        topic = str(row.get("topic", "")).strip().lower()
        subtopic = str(row.get("subtopic", "")).strip().lower()

        if qv:
            terms.add(qv)
        if topic:
            terms.add(topic)
        if subtopic:
            terms.add(subtopic)

    return sorted(terms)


def correct_short_query(query: str, kb_terms: list[str]) -> str:
    q = query.strip().lower()

    if len(q.split()) <= 3 and "?" not in q:
        matches = get_close_matches(q, kb_terms, n=1, cutoff=0.78)
        if matches:
            return matches[0]

    return query
    
class Chatbot:
    def __init__(self):
        print("Loading intent classifier...")
        self.clf = joblib.load(MODEL_PATH)

        print("Loading retriever...")
        self.retriever = Retriever()

    def predict_intent(self, query: str):
        query_clean = clean_text(query)
        return self.clf.predict([query_clean])[0]

    def respond(self, query: str):
        # 0. Normalize short fragment queries
        query = correct_short_query(query, self.kb_terms)
        query = normalize_short_query(query)

        # 1. Rule-based override first
        override_intent = rule_based_override(query)

        if override_intent is not None:
            intent = override_intent
        else:
            intent = self.predict_intent(query)

        # 2. If still OOS, check if the question looks academic
        if intent == "oos" and not looks_academic(query):
            return {
                "intent": intent,
                "response": "Sorry, I cannot answer that question."
            }

        # 3. If OOS but still academic-looking, force concept/course retrieval
        if intent == "oos" and looks_academic(query):
            intent = "concept_query"

        # 4. Retrieval
        docs = self.retriever.retrieve(query, top_k=7, predicted_intent=intent)

        # 5. Low-confidence retrieval guard
        if not docs or docs[0]["score"] < 0.45:
            return {
                "intent": intent,
                "response": "I am not confident that this question is covered by the academic knowledge base.",
                "retrieved_docs": docs
            }

        # 6. Lightweight grounded generation
        answer = generate_answer(query, docs)

        return {
            "intent": intent,
            "response": answer,
            "retrieved_docs": docs
        }
